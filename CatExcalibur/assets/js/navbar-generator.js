document.addEventListener('DOMContentLoaded', initializeNavbar);

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏µ‡πà‡∏£‡∏±‡∏ô‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏ß‡πá‡∏ö‡πÇ‡∏´‡∏•‡∏î‡πÄ‡∏™‡∏£‡πá‡∏à
function initializeNavbar() {
    // 1. ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• User Role, CustomerID, ‡πÅ‡∏•‡∏∞ Username ‡∏à‡∏≤‡∏Å localStorage
    const userRole = localStorage.getItem('UserRole');
    const customerId = localStorage.getItem('CustomerID');
    const username = localStorage.getItem('Username');
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ Login ‡πÅ‡∏•‡πâ‡∏ß‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
    const isLoggedIn = !!customerId;
    
    // 2. ‡∏Å‡∏≥‡∏´‡∏ô‡∏î Element ‡∏ó‡∏µ‡πà‡∏à‡∏∞‡πÉ‡∏´‡πâ‡πÇ‡∏Ñ‡πâ‡∏î‡∏™‡∏£‡πâ‡∏≤‡∏á Navbar
    const navbarContainer = document.getElementById('main-navbar'); 
    
    if (!navbarContainer) {
        console.error("Error: Navbar container element with ID 'main-navbar' not found.");
        return;
    }

    // 3. ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡πÇ‡∏Ñ‡∏£‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏ï‡∏≤‡∏° Role
    const linksMap = getRoleBasedLinks(isLoggedIn, userRole);
    
    // 4. ‡∏™‡∏£‡πâ‡∏≤‡∏á Navbar HTML
    let navbarHTML = '';

    // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°‡∏Å‡∏±‡∏ö Role
    linksMap.mainLinks.forEach(link => {
        navbarHTML += `<a href="${link.href}" class="nav-link">${link.name}</a>`;
    });
    
    // 5. ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏õ‡∏∏‡πà‡∏° Profile ‡∏´‡∏£‡∏∑‡∏≠ Login/Register (‡πÅ‡∏¢‡∏Å‡∏ï‡πà‡∏≤‡∏á‡∏´‡∏≤‡∏Å)
    // üí° ‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡πÅ‡∏¢‡∏Å‡∏™‡πà‡∏ß‡∏ô‡∏ô‡∏µ‡πâ‡∏à‡∏∞‡∏ä‡πà‡∏ß‡∏¢‡πÉ‡∏´‡πâ Profile Icon ‡πÑ‡∏°‡πà‡∏ã‡πâ‡∏≥‡∏ã‡πâ‡∏≠‡∏ô‡∏Å‡∏±‡∏ö‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏≠‡∏∑‡πà‡∏ô‡πÜ
    navbarHTML += generateAuthLink(isLoggedIn, username);
    
    // 6. ‡πÅ‡∏ó‡∏£‡∏Å HTML ‡∏ó‡∏µ‡πà‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÉ‡∏´‡∏°‡πà‡πÄ‡∏Ç‡πâ‡∏≤‡πÑ‡∏õ‡πÉ‡∏ô Container
    navbarContainer.innerHTML = navbarHTML;
}

