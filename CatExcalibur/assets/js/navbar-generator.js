function getRoleBasedLinks(isLoggedIn, userRole) {
    
    // ‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏ó‡∏µ‡πà‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏ô‡πÄ‡∏´‡πá‡∏ô (Public - ‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡∏Ñ‡∏ß‡∏ö‡∏Ñ‡∏∏‡∏°‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå)
    const publicLinks = [
        { name: 'Home', href: '../index.html' },
        { name: 'Rewards & Promotions', href: 'reward.html' } 
    ];

    // ‡∏•‡∏¥‡∏á‡∏Å‡πå‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å (‡∏™‡∏¥‡πà‡∏á‡∏ó‡∏µ‡πà‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤‡πÄ‡∏´‡πá‡∏ô‡∏ô‡∏≠‡∏Å‡πÄ‡∏´‡∏ô‡∏∑‡∏≠‡∏à‡∏≤‡∏Å Public)
    const customerLinks = [
        { name: 'Booking', href: 'reservation.html' },
        { name: 'My Reservations', href: 'my_reservations.html' }
    ];

    // ‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏û‡∏ô‡∏±‡∏Å‡∏á‡∏≤‡∏ô/‡∏ú‡∏π‡πâ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ (‡∏á‡∏≤‡∏ô‡∏´‡∏•‡∏±‡∏á‡∏ö‡πâ‡∏≤‡∏ô)
    const adminLinks = [
        { name: 'Checkout', href: 'checkout.html' },
        { name: 'Manage Reservations', href: 'reservation_management.html' },
        { name: 'Sales History', href: 'sales_history.html' } 
    ];

    let mainLinks = publicLinks; // ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô‡∏î‡πâ‡∏ß‡∏¢‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏ó‡∏µ‡πà‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏ô‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á‡πÑ‡∏î‡πâ
    
    if (isLoggedIn) {
        if (userRole === 'Admins') {
            // Admin: ‡πÄ‡∏´‡πá‡∏ô Public + ‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£
            mainLinks = mainLinks.concat(adminLinks);
        } else {
            // Customer: ‡πÄ‡∏´‡πá‡∏ô Public + ‡∏•‡∏¥‡∏á‡∏Å‡πå‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤
            mainLinks = mainLinks.concat(customerLinks);
        }
    }
    // ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Guest: mainLinks ‡∏à‡∏∞‡∏°‡∏µ‡πÅ‡∏Ñ‡πà Public Links
    
    return { mainLinks: mainLinks };
}

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏õ‡∏∏‡πà‡∏° Login/Profile (‡∏ó‡∏≥‡∏´‡∏ô‡πâ‡∏≤‡∏ó‡∏µ‡πà‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏õ‡∏∏‡πà‡∏°‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô)
function generateAuthLink(isLoggedIn, username) {
    if (isLoggedIn) {
        // ‡∏ñ‡πâ‡∏≤ Login ‡πÅ‡∏•‡πâ‡∏ß: ‡πÅ‡∏™‡∏î‡∏á‡πÑ‡∏≠‡∏Ñ‡∏≠‡∏ô Profile
        // üí° ‡πÄ‡∏£‡∏≤‡πÉ‡∏ä‡πâ 'member_profile.html' ‡πÄ‡∏õ‡πá‡∏ô‡∏•‡∏¥‡∏á‡∏Å‡πå Profile 
        return `<a href="member_profile.html" class="nav-link profile-link" title="${username}">üë§</a>`; 
    } else {
        // ‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà Login: ‡πÅ‡∏™‡∏î‡∏á‡∏õ‡∏∏‡πà‡∏° Login/Register
        return `<a href="login.html" class="nav-link">Login</a>`;
    }
}

