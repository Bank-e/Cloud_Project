    if (!reservationForm) {
        console.error('Reservation form not found. (Must have id="reservationForm")');
        return;
    }

    // üí° NEW: ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ min attribute ‡πÉ‡∏´‡πâ‡∏Å‡∏±‡∏ö input type="date"
    // ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô‡∏Å‡∏≤‡∏£‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ß‡∏±‡∏ô‡πÉ‡∏ô‡∏≠‡∏î‡∏µ‡∏ï‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏ï‡πà‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô‡πÉ‡∏ô‡∏£‡∏∞‡∏î‡∏±‡∏ö UI/Browser
